import Head from "next/head";
import Image from "next/image";
import { useState } from "react";
import Button from "../components/Button";
import Canvas from "../components/Canvas";
import Input from "../components/Input";

const colors = ["#99ccff", "#ff7799", "#77ff99", "#ffffaa"];

export default function Home() {
  const [functions, setFunctions] = useState([
    {
      expression: "x",
      color: "#99ccff",
    },
  ]);
  const [tab, setTab] = useState(0);

  return (
    <div>
      <Head>
        <title>Graphing Calculator</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <div className="p-5 min-w-[100%]">
        <h1 className="text-4xl bold">Graphing Calculator</h1>
        <div className="h-[1px] w-[100%] lg:w-[100%] bg-silver"></div>
        <div className="flex flex-col lg:flex-row min-h-[500px]">
          <div className="w-full">
            {tab == 0 && (
              <div className="flex flex-col lg:flex-row">
                <Canvas functions={functions} />
                <div className="m-0 lg:m-1 p-0 border-t-[1px] lg:border-none border-white w-[100%]">
                  {[...functions].map((f, i) => (
                    <div
                      key={i}
                      className="m-1 py-1 border-b border-dashed flex flex-row align-middle"
                    >
                      <span className="mr-1 self-center">{i + 1}.</span>
                      <Input
                        val={f.expression}
                        onChange={(t) => {
                          functions[i].expression = t;
                          setFunctions(functions);
                        }}
                      />
                    </div>
                  ))}
                  <button
                    className="border-dashed border m-2 p-1 hover:border-solid transition hover:bg-blue-bg no-tap"
                    onClick={() => {
                      const a = functions;
                      a.push({
                        expression: "",
                        color: colors[a.length % colors.length],
                      });
                      setFunctions([...a]);
                    }}
                  >
                    Add Function
                  </button>
                </div>
                <div className="hidden lg:block w-[100%]"></div>
              </div>
            )}
            {tab == 2 && (
              <p className="m-0 p-0 pr-8">
                <br />
                This is a graphing calculator built in JavaScript using React
                and the canvas. This graphing calculator can graph any
                polynomial, logarithmic, trigonometric, or exponential function.
                It uses the library expr-eval for mathematical evaluation.
              </p>
            )}
          </div>
          <div className="bg-striped pl-2 pb-2 mt-4 mb-4 lg:mt-0 lg:pl-0 lg: pb-2 lg:h-100 lg:w-12 flex flex-row lg:flex-col rounded-xl lg:rounded-none lg:rounded-br-xl hblock">
            <div
              className={
                (tab == 0 ? "bg-dark" : "") +
                " w-10 h-10 mt-2 flex items-center justify-center rounded lg:rounded-none lg:rounded-r-lg cursor-pointer transition no-tap"
              }
              onClick={() => setTab(0)}
            >
              <svg
                xmlns="http://www.w3.org/2000/svg"
                width={24}
                height={24}
                viewBox="0 0 24 24"
                fill="none"
                stroke="currentColor"
                strokeWidth={2}
                strokeLinecap="round"
                strokeLinejoin="round"
                className=""
              >
                <line x1={18} y1={20} x2={18} y2={10} />
                <line x1={12} y1={20} x2={12} y2={4} />
                <line x1={6} y1={20} x2={6} y2={14} />
              </svg>
            </div>
            <div
              className={
                (tab == 1 ? "bg-dark" : "") +
                " w-10 h-10 mt-2 flex items-center justify-center rounded lg:rounded-none lg:rounded-r-lg cursor-pointer transition lg:hidden no-tap"
              }
              onClick={() => setTab(1)}
            >
              <svg
                xmlns="http://www.w3.org/2000/svg"
                width={24}
                height={24}
                viewBox="0 0 24 24"
                fill="none"
                stroke="currentColor"
                strokeWidth={2}
                strokeLinecap="round"
                strokeLinejoin="round"
                className=""
              >
                <rect x={3} y={3} width={7} height={7} />
                <rect x={14} y={3} width={7} height={7} />
                <rect x={14} y={14} width={7} height={7} />
                <rect x={3} y={14} width={7} height={7} />
              </svg>
            </div>
            <div
              className={
                (tab == 2 ? "bg-dark" : "") +
                " w-10 h-10 mt-2 flex items-center justify-center rounded lg:rounded-none lg:rounded-r-lg cursor-pointer transition no-tap"
              }
              onClick={() => setTab(2)}
            >
              <svg
                xmlns="http://www.w3.org/2000/svg"
                width={24}
                height={24}
                viewBox="0 0 24 24"
                fill="none"
                stroke="currentColor"
                strokeWidth={2}
                strokeLinecap="round"
                strokeLinejoin="round"
              >
                <circle cx={12} cy={12} r={10} />
                <path d="M9.09 9a3 3 0 0 1 5.83 1c0 2-3 3-3 3" />
                <line x1={12} y1={17} x2="12.01" y2={17} />
              </svg>
            </div>
          </div>
        </div>
      </div>
    </div>
  );
}
